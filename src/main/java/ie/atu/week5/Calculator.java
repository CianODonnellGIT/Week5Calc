package ie.atu.week5;

public class Calculator {
    private int num1;
    private int num2;

    public int add(){
        return num1+num2;
    }

    public int sub(){
        return num1-num2;
    }

    public int multiply(){
        return num1*num2;
    }

    public int divide(){
        return num1/num2;
    }

}
